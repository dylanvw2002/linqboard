import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_ANON_KEY') ?? '',
      {
        global: {
          headers: { Authorization: req.headers.get('Authorization')! },
        },
      }
    );

    const { data: { user } } = await supabaseClient.auth.getUser();
    if (!user) {
      throw new Error('Not authenticated');
    }

    const { new_plan, billing_interval } = await req.json();

    console.log('Scheduling plan change:', { user_id: user.id, new_plan, billing_interval });

    // Update the subscription with pending plan
    const { error: updateError } = await supabaseClient
      .from('user_subscriptions')
      .update({
        pending_plan: new_plan,
        pending_billing_interval: billing_interval
      })
      .eq('user_id', user.id);

    if (updateError) throw updateError;

    console.log('Successfully scheduled plan change for next billing cycle');

    return new Response(
      JSON.stringify({ 
        success: true,
        message: 'Plan change scheduled for next billing cycle'
      }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200 
      }
    );

  } catch (error) {
    console.error('Error scheduling plan change:', error);
    const errorMessage = error instanceof Error ? error.message : 'Unknown error';
    return new Response(
      JSON.stringify({ error: errorMessage }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400 
      }
    );
  }
});